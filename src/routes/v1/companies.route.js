const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const companiesValidation = require('../../validations/companies.validation');
const companiesController = require('../../controllers/companies.controller');

const router = express.Router();

router
  .route('/')
  .post( validate(companiesValidation.createCompanies), companiesController.createCompanies)
  .get( validate(companiesValidation.getCompaniess), companiesController.getCompaniess);

router
  .route('/:companiesId')
  .get(validate(companiesValidation.getCompanies), companiesController.getCompanies)
  .patch( validate(companiesValidation.updateCompanies), companiesController.updateCompanies)
  .delete( validate(companiesValidation.deleteCompanies), companiesController.deleteCompanies);

module.exports = router;

